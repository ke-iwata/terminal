#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn theme_names_are_unique() {
        let mut names: Vec<&str> = THEMES.iter().map(|(name, _)| *name).collect();
        let count = names.len();
        names.sort_unstable();
        names.dedup();
        assert_eq!(names.len(), count, "duplicate theme name in THEMES");
    }

    #[test]
    fn default_dark_matches_palette_default() {
        let (_, palette) = THEMES.iter().find(|(name, _)| *name == "Default Dark").unwrap();
        assert_eq!(*palette, Palette::default());
    }
}
